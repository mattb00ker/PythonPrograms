# Works between the 9 and 15th of September 2024

date = int(input("What is today's date?"))

if(date == 9):
    print("It's Monday")
elif(date == 10):
    print("It's Tuesday")
elif(date == 11):
    print("It's Wednesday")
elif(date == 12):
    print("It's Thursday")
elif(date == 13):
    print("It's Friday")
elif(date == 14):
    print("It's Saturday")
elif(date == 15):
    print("It's Sunday")
else:
    print("I'm sorry, that's outside my range")
